public class Date {

    private int dia;
    private int mes;
    private int ano;

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public int getMes(){
        return mes;
    }

    public void setMes(){
        this.mes = mes;
    }

    public int getDia(){
        return dia;
    }

    public void setDia (){
        this.dia = dia;
    }

}
